/**
 * Main execution
 */
async function main() {
  try {
    console.log('Starting PR review...');
    console.log(`PR #${config.prNumber} in ${config.repoOwner}/${config.repoName}`);

    // Fetch PR data
    console.log('Fetching PR diff and files...');
    const [diff, files] = await Promise.all([
      getPRDiff(),
      getPRFiles()
    ]);

    console.log(`Found ${files.length} changed files`);

    // Analyze with AI
    console.log('Analyzing code with AI...');
    const review = await analyzeCodeWithAI(diff, files);

    // Post review
    console.log('Posting review comment...');
    await postReviewComment(review);

    console.log('PR review completed successfully! ✅');
  } catch (error) {
    console.error('Failed to complete PR review:', error);
    process.exit(1);
  }
}

// Run the main function
main();

